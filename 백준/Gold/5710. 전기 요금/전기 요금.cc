#include<bits/stdc++.h>
using namespace std;

int main()
{
	while(1)
	{
		long long a, b;
		scanf("%lld%lld", &a, &b);
		if(a==0&&b==0) return 0;
		if(a<=200)
		{
			a = a/2;
		}
		else if(a<=200+9900*3)
		{
			a = 100+(a-200)/3;
		}
		else if(a<=200+9900*3+990000*5)
		{
			a = 10000+(a-200-9900*3)/5;
		}
		else
		{
			a = 1000000+(a-200-9900*3-990000*5)/7;
		}
		int x, y;
		//x<=100, y<=100 
		x = (2*a-b)/4, y = a-x;
		if(x<=100&&y<=100&&2*y-2*x==b)
		{
			printf("%lld\n", x*2);
			continue;
		}
		//x<=100, y<=10000 
		x = (3*a-b-100)/5, y = a-x;
		if(x<=100&&y>100&&y<=10000&&3*(y-100)+200-2*x==b)
		{
			printf("%lld\n", x*2);
			continue;
		}
		//x<=100, y<=1000000
		x = (5*a-b-20100)/7, y = a-x;
		if(x<=100&&y>10000&&y<=1000000&&5*(y-10000)+29900-2*x==b)
		{
			printf("%lld\n", x*2);
			continue;
		}
		//x<=100, y>1000000
		x = (7*a-b-2020100)/9, y = a-x;
		if(x<=100&&y>1000000&&7*(y-1000000)+4979900-2*x==b)
		{
			printf("%lld\n", x*2);
			continue;
		}
		//x<=10000, y<=10000 
		x = (3*a-b)/6, y = a-x;
		if(x>100&&x<=10000&&y>100&&y<=10000&&3*y-3*x==b)
		{
			printf("%lld\n", (x-100)*3+200);
			continue;
		}
		//x<=10000, y<=1000000
		x = (5*a-b-20000)/8, y = a-x;
		if(x>100&&x<=10000&&y>10000&&y<=1000000&&5*y-3*x-20000==b)
		{
			printf("%lld\n", (x-100)*3+200);
			continue;
		}
		//x<=10000, y>1000000
		x = (7*a-b-2020000)/10, y = a-x;
		if(x>100&&x<=10000&&y>1000000&&7*y-3*x-2020000==b)
		{
			printf("%lld\n", (x-100)*3+200);
			continue;
		}
		//x<=1000000, y<=1000000
		x = (5*a-b)/10, y = a-x;
		if(x>10000&&x<=1000000&&y>10000&&y<=1000000&&5*y-5*x==b)
		{
			printf("%lld\n", (x-10000)*5+200+9900*3);
			continue;
		}
		//x<=1000000, y>1000000
		x = (7*a-b-2000000)/12, y = a-x;
		if(x>10000&&x<=1000000&&y>1000000&&7*y-5*x-2000000==b)
		{
			printf("%lld\n", (x-10000)*5+200+9900*3);
			continue;
		}
		
		x = (7*a-b)/14, y = a-x;
		if(x>1000000&&y>1000000&&7*y-7*x==b)
		{
			printf("%lld\n", (x-1000000)*7+200+9900*3+990000*5);
			continue;
		}
	}
}